void main (){
    int x;
    x = 10; // This is an identity conversion.
    double y;
    y = 5.2;// This is an identity conversion.
    float f;
    f = 25.2f; // This is an identity conversion.
    String str;
    str = "Hello"; // This is an identity conversion.
    char c = 'c'; // This is an identity conversion.
    long l = 10l; // // This is an identity conversion.
    double d = 10; // This is not an identity conversion.
    float f2 = 15; // This is not an identity conversion.
    byte b = 15; // This is not an identity conversion.
    str = null; // This is not an identity conversion.
    c = 75; // This is not an identity conversion.
}